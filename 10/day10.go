// GetInput processes proper puzzle input
func GetInput() [][]rune {
	return ProcessInput(input)
}

// ProcessInput processes arbitrary puzzle input
func ProcessInput(in string) [][]rune {
	rows := strings.Split(in, "\n")
	asteroids := make([][]rune, len(rows))
	for i, r := range rows {
		for _, c := range r {
			asteroids[i] = append(asteroids[i], c)
		}
	}

	return asteroids
}
